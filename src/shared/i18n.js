  function normalizeKey(value) {
    return String(value || "").replace(/\s+/g, " ").trim();
  }

  function normalizeLocale(value) {
    const raw = String(value || "").replace(/_/g, "-");
    if (!raw) return "en";
    if (ALIASES[raw]) return ALIASES[raw];
    if (SUPPORTED.includes(raw)) return raw;
    const base = raw.split("-")[0];
    if (ALIASES[base]) return ALIASES[base];
    if (SUPPORTED.includes(base)) return base;
    return "en";
  }

  function detectLocale() {
    const candidates = []
      .concat((root.navigator && Array.isArray(root.navigator.languages)) ? root.navigator.languages : [])
      .concat(root.navigator && root.navigator.language ? [root.navigator.language] : []);
    for (const candidate of candidates) {
      const locale = normalizeLocale(candidate);
      if (locale !== "en") return locale;
    }
    return "en";
  }

  const browserLocale = detectLocale();
  let currentLocale = browserLocale;
  let localePreference = "auto";
  const textSourceByNode = new WeakMap();
  const attrSourceByElement = new WeakMap();

  function resolveLocalePreference(value) {
    const raw = String(value || "auto").trim();
    return raw === "auto" ? "auto" : normalizeLocale(raw);
  }

  function resolveLocale(value) {
    const preference = resolveLocalePreference(value);
    return preference === "auto" ? browserLocale : normalizeLocale(preference);
  }

  function setLocale(value) {
    localePreference = resolveLocalePreference(value);
    currentLocale = resolveLocale(localePreference);
    return currentLocale;
  }

  function applySettings(settings) {
    const preference = settings && Object.prototype.hasOwnProperty.call(settings, "uiLanguage")
      ? settings.uiLanguage
      : "auto";
    return setLocale(preference);
  }

  function t(message, vars) {
    const source = String(message || "");
    if (!source) return "";
    const dict = translations[currentLocale] || {};
    const normalized = normalizeKey(source);
    let translated = dict[source] || dict[normalized] || source;
    if (vars && typeof vars === "object") {
      Object.keys(vars).forEach((key) => {
        translated = translated.replace(new RegExp("\\{" + key + "\\}", "g"), String(vars[key]));
      });
    }
    return translated;
  }

  function shouldSkipTextNode(node) {
    const parent = node && node.parentElement;
    if (!parent) return true;
    if (/^(SCRIPT|STYLE|TEXTAREA|INPUT|PRE|CODE)$/i.test(parent.tagName || "")) return true;
    return !!(parent.closest && parent.closest("[data-i18n-skip]"));
  }

  function translateTextNode(node) {
    if (shouldSkipTextNode(node)) return;
    const raw = node.nodeValue || "";
    const normalized = textSourceByNode.get(node) || normalizeKey(raw);
    if (!normalized) return;
    if (!textSourceByNode.has(node)) {
      textSourceByNode.set(node, normalized);
    }
    const translated = t(normalized);
    if (translated === normalized && normalizeKey(raw) === normalized) return;
    const leading = raw.match(/^\s*/)[0] || "";
    const trailing = raw.match(/\s*$/)[0] || "";
    node.nodeValue = `${leading}${translated}${trailing}`;
  }

  function translateAttributes(element) {
    if (element.closest && element.closest("[data-i18n-skip]")) return;
    let attrSources = attrSourceByElement.get(element);
    if (!attrSources) {
      attrSources = {};
      attrSourceByElement.set(element, attrSources);
    }
    ["title", "aria-label", "placeholder"].forEach((attr) => {
      if (!element.hasAttribute || !element.hasAttribute(attr)) return;
      const value = attrSources[attr] || element.getAttribute(attr);
      const normalized = normalizeKey(value);
      if (!normalized) return;
      if (!attrSources[attr]) {
        attrSources[attr] = normalized;
      }
      element.setAttribute(attr, t(normalized));
    });
  }

  function localize(rootNode) {
    const rootEl = rootNode || root.document;
    if (!rootEl) return;
    const doc = rootEl.nodeType === 9 ? rootEl : rootEl.ownerDocument;
    if (doc && doc.documentElement) {
      doc.documentElement.lang = currentLocale;
    }
    if (doc && doc.title) {
      doc.title = t(doc.title);
    }
    if (rootEl.nodeType === 1) {
      translateAttributes(rootEl);
    }
    const walkerRoot = rootEl.nodeType === 9 ? rootEl.body || rootEl.documentElement : rootEl;
    if (!walkerRoot) return;
    const nodeFilter = (doc && doc.defaultView && doc.defaultView.NodeFilter) || root.NodeFilter;
    if (!nodeFilter) return;
    const walker = (doc || root.document).createTreeWalker(
      walkerRoot,
      nodeFilter.SHOW_TEXT | nodeFilter.SHOW_ELEMENT
    );
    let node = walker.currentNode;
    while (node) {
      if (node.nodeType === 1) {
        translateAttributes(node);
      } else if (node.nodeType === 3) {
        translateTextNode(node);
      }
      node = walker.nextNode();
    }
  }

