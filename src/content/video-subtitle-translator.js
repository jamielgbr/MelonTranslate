      state.renderedOverlayMode === "takeover"
      && state.renderedOverlaySourceText === sourceValue
      && state.renderedOverlayTargetText === targetValue
      && state.renderedOverlayNextText === nextValue
      && state.renderedOverlayHidden === hidden
