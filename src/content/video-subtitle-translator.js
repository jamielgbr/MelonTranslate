  function serializeSubtitleCueForExport(cue, index) {
    const id = String(cue && cue.id || index);
    const start = Number(cue && cue.start);
    const end = Number(cue && cue.end);
    const translatedText = String(state.translations.get(id) || "");
    const item = {
      index,
      id,
      start: Number.isFinite(start) ? start : null,
      end: Number.isFinite(end) ? end : null,
      text: String(cue && cue.text || ""),
      translatedText,
      translationStatus: getCueTranslationStatus(id, translatedText)
    };
    if (Number.isFinite(start) && Number.isFinite(end) && end > start) {
      item.duration = end - start;
    }
    if (cue && Array.isArray(cue.cueIds)) {
      item.cueIds = cue.cueIds.map((cueId) => String(cueId));
    }
    if (cue && cue.forceBoundaryAfter) {
      item.forceBoundaryAfter = true;
    }
    if (cue && cue.hasSegmentTiming) {
      item.hasSegmentTiming = true;
    }
    if (cue && cue.timingEstimated) {
      item.timingEstimated = true;
    }
    return item;
  }

  function serializeDomSubtitleCuesForExport() {
    const items = [];
    state.domSourceById.forEach((text, id) => {
      const sourceText = normalizeRenderedCaptionText(text);
      if (!sourceText) {
        return;
      }
      const translatedText = String(state.translations.get(id) || "");
      items.push({
        id: String(id),
        text: sourceText,
        translatedText,
        translationStatus: getCueTranslationStatus(String(id), translatedText)
      });
    });
    const liveText = normalizeRenderedCaptionText(state.domCaptionText || "");
    if (liveText && !items.some((item) => item.text === liveText)) {
      const id = buildTextCueId(liveText);
      const translatedText = String(state.translations.get(id) || findBestDomPrefixTranslation(liveText) || "");
      items.push({
        id,
        text: liveText,
        translatedText,
        translationStatus: getCueTranslationStatus(id, translatedText),
        live: true
      });
    }
    return items;
  }

